def run_worker(args):
    import torch
    from vllm import LLM, SamplingParams
    
    mode = args.run_mode
    
    if mode != "A_vanilla":
        from integrations.vllm_route_plugin.patch import apply_all_patches
        from integrations.vllm_route_plugin.runtime_context import get_vllm_registry
        
        apply_all_patches()
        instrument_runtime()
        
        registry = get_vllm_registry()
        target_layers = [{
            "name": "model.decoder.layers.0.self_attn.qkv_proj.weight",
            "shape": [2304, 768],
            "dtype": "float16"
        }]

        if mode in {"C_simulated_routes", "E_mixed_route"}:
            registry.routes["simulated-route"] = {
                "route_id": "simulated-route",
                "tenant_id": "bench",
                "layers": target_layers,
                "payload_type": "simulated"
            }

        if mode in {"D_safetensors_routes", "E_mixed_route"}:
            payload_uri, payload_sha256 = ensure_benchmark_payload()

            registry.routes["sql-route"] = {
                "route_id": "sql-route",
                "tenant_id": "bench",
                "layers": target_layers,
                "payload_type": "real",
                "payload": {
                    "format": "safetensors",
                    "uri": payload_uri,
                    "sha256": payload_sha256,
                }
            }
        
    print(f"[WORKER] Initializing Engine for {mode}...")
    vram_initial = torch.cuda.memory_reserved() / 1024**2 if torch.cuda.is_available() else 0.0
    
    llm = LLM(model="facebook/opt-125m", enforce_eager=True)
    
    prompts = ["List the prime numbers up to 100."] * args.prompts
    
    sps = []
    routes = ["__base__", "sql-route", "simulated-route"]
    for i in range(args.prompts):
        sp = SamplingParams(max_tokens=32, temperature=0.0)
        
        if mode == "B_base_route":
            sp.extra_args = {"route_id": "__base__"}
        elif mode == "C_simulated_routes":
            sp.extra_args = {"route_id": "simulated-route"}
        elif mode == "D_safetensors_routes":
            sp.extra_args = {"route_id": "sql-route"}
        elif mode == "E_mixed_route":
            sp.extra_args = {"route_id": routes[i % len(routes)]}
            
        sps.append(sp)
        
    print("[WORKER] Generating...")
    
    t0 = time.perf_counter()
    outputs = llm.generate(prompts, sps, use_tqdm=False)
    t1 = time.perf_counter()
    
    vram_peak = torch.cuda.memory_reserved() / 1024**2 if torch.cuda.is_available() else 0.0
    
    ttfts = []
    tpots = []
    e2es = []
    total_tokens = 0
    
    for out in outputs:
        # Extract TTFT/TPOT from RequestOutput.metrics if available
        metrics = getattr(out, 'metrics', None)
        if metrics is not None:
            arrival = getattr(metrics, 'arrival_time', None)
            first_tok = getattr(metrics, 'first_token_time', None)
            finished = getattr(metrics, 'finished_time', None)
            
            if arrival and first_tok and finished:
                ttfts.append(first_tok - arrival)
                e2es.append(finished - arrival)
                
                output_len = len(out.outputs[0].token_ids)
                if output_len > 1:
                    tpots.append((finished - first_tok) / (output_len - 1))
                else:
                    tpots.append(0.0)
        
        total_tokens += len(out.outputs[0].token_ids)
        
    e2e_total = t1 - t0
    req_sec = args.prompts / e2e_total if e2e_total > 0 else 0
    tok_sec = total_tokens / e2e_total if e2e_total > 0 else 0
    
    def percentile(lst, p):
        if not lst: return None
        s = sorted(lst)
        k = (len(s) - 1) * p
        f = int(k)
        c = min(f + 1, len(s) - 1)
        return s[f] + (k - f) * (s[c] - s[f])

    results = {
        "ttft_p50": percentile(ttfts, 0.5),
        "ttft_p90": percentile(ttfts, 0.9),
        "ttft_p99": percentile(ttfts, 0.99),
        "tpot_p50": percentile(tpots, 0.5),
        "tpot_p90": percentile(tpots, 0.9),
        "tpot_p99": percentile(tpots, 0.99),
        "e2e_p50": percentile(e2es, 0.5) if e2es else (e2e_total / args.prompts if args.prompts > 0 else None),
        "req_sec": req_sec,
        "tok_sec": tok_sec,
        "vram_initial_mb": vram_initial,
        "vram_peak_mb": vram_peak,
        "swap_p50": percentile(swap_latencies, 0.5),
        "rollback_p50": percentile(rollback_latencies, 0.5),
        "validation_p50": percentile(validation_latencies, 0.5),
        "load_p50": percentile(load_latencies, 0.5),
    }
    
    with open(args.json_out, "w") as f:
        json.dump(results, f, indent=2)


