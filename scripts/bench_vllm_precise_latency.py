# We will run this script as a master script that invokes itself with different modes
# to ensure clean process state for vanilla vs patched vLLM.

def fmt_ms(value: Optional[float]) -> str:
    if value is None:
        return "not captured"
    return f"{value:.2f} ms"

def fmt_td_ms(value: Optional[float]) -> str:
    if value is None:
        return "N/A"
    return f"{value:.1f}"

def run_in_subprocess(mode: str, prompts: int) -> Dict[str, Any]:
    print(f"\n[BENCHMARK] Running mode: {mode} with {prompts} prompts...")
    cmd = [
        sys.executable, __file__,
        "--run-mode", mode,
        "--prompts", str(prompts),
        "--json-out", f"reports/bench_raw_{mode}.json"
    ]
    env = None
    subprocess.run(cmd, env=env, check=True)
    
    with open(f"reports/bench_raw_{mode}.json", "r") as f:
        return json.load(f)

