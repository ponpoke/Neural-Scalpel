swap_latencies = []
rollback_latencies = []
validation_latencies = []
load_latencies = []

def instrument_runtime():
    """Monkey-patch HotSwapRuntime to measure internal GPU/CPU timings."""
    import torch
    try:
        from neural_scalpel.experimental.runtime import HotSwapRuntime
    except ImportError:
        return
        
    orig_apply_swap = HotSwapRuntime.apply_swap
    orig_perform_rollback = HotSwapRuntime._perform_rollback
    
    def hooked_apply_swap(self, route_data, validated_deltas=None):
        start_event = torch.cuda.Event(enable_timing=True)
        end_event = torch.cuda.Event(enable_timing=True)
        start_event.record()
        
        res = orig_apply_swap(self, route_data, validated_deltas)
        
        end_event.record()
        torch.cuda.synchronize()
        swap_latencies.append(start_event.elapsed_time(end_event))
        return res
        
    def hooked_perform_rollback(self, *args, **kwargs):
        start_event = torch.cuda.Event(enable_timing=True)
        end_event = torch.cuda.Event(enable_timing=True)
        start_event.record()
        
        res = orig_perform_rollback(self, *args, **kwargs)
        
        end_event.record()
        torch.cuda.synchronize()
        rollback_latencies.append(start_event.elapsed_time(end_event))
        return res
        
    HotSwapRuntime.apply_swap = hooked_apply_swap
    HotSwapRuntime._perform_rollback = hooked_perform_rollback
    
    # We can also patch validate_payload / load_payload_deltas if imported
    try:
        import neural_scalpel.route.payload_validator as pv
        orig_validate = pv.validate_payload
        def hooked_validate(*args, **kwargs):
            t0 = time.perf_counter()
            res = orig_validate(*args, **kwargs)
            validation_latencies.append((time.perf_counter() - t0) * 1000)
            return res
        pv.validate_payload = hooked_validate
    except AttributeError:
        pass

    try:
        import neural_scalpel.route.payload as pyld
        orig_load = pyld.load_payload_deltas
        def hooked_load(*args, **kwargs):
            t0 = time.perf_counter()
            res = orig_load(*args, **kwargs)
            load_latencies.append((time.perf_counter() - t0) * 1000)
            return res
        pyld.load_payload_deltas = hooked_load
    except AttributeError:
        pass

def sha256_file(path):
    import hashlib
    from pathlib import Path

    path = Path(path)
    h = hashlib.sha256()
    with path.open("rb") as f:
        for chunk in iter(lambda: f.read(1024 * 1024), b""):
            h.update(chunk)
    return h.hexdigest()

